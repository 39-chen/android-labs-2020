package edu.hzuapps.androidlabs.sec1814080911238;

public class Fruit {
    private String name;
    private String imageId;

    public Fruit(String name, String imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public String getImageId() {
        return imageId;
    }
}
